package im.kuka.springboot.demo.config;

import im.kuka.springboot.demo.Interceptor.LoginInterceptor;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.InterceptorRegistry;
import org.springframework.web.servlet.config.annotation.ResourceHandlerRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurerAdapter;

/**
 * @author: shipeng.yu
 * @time: 2016年09月07日 上午2:02
 * @version: 1.0
 * @since: 1.0
 * @description: Web Config 配置类
 */
@Configuration
public class WebMvcConfig extends WebMvcConfigurerAdapter {

    @Override
    public void addResourceHandlers(ResourceHandlerRegistry registry) {
        registry.addResourceHandler("/static/**").addResourceLocations("classpath:/static/");
    }

    @Override
    public void addInterceptors(InterceptorRegistry registry) {
        // 配置登录拦截器
        registry.addInterceptor(new LoginInterceptor()).addPathPatterns(LoginInterceptor.pathPatterns);
        super.addInterceptors(registry);
    }

}
